     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        $project=Project::findOrFail($id);
        if (auth()->user()->id == $project->users_id) {
            return view('projects.show', [ 'project'=>$project ]);
        } else {
            return abort(403);
        }
    }

